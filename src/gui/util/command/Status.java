public enum Status {
        IDLE,
        WAITING,
        RUNNING,
        DONE,
        INTERRUPPTED,
        ERROR;

        @Override
        public String toString() {
            switch (this) {
                case IDLE:
                    return "空闲";
                case WAITING:
                    return "等待";
                case RUNNING:
                    return "运行";
                case DONE:
                    return "完成";
                case INTERRUPPTED:
                    return "中断";
                case ERROR:
                    return "ERROR";
                default:
                    return "UNKNOWN";
            }

        }
    }
