public class ColumnWidth {

    static int getWidth(ColumnType columnType) {
        int width = 0;
        switch (columnType) {
            case SOURCE_FILE:
            case DEST_DIR:
            case DEST_NAME:
            case PROFILE:
            case SCRIPT:
            case TIME_RANGE:
                width =  140;
                break;
        }
        return width;
    }
}
